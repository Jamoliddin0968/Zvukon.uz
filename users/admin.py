from django.contrib import admin
from users.models import User , Account


@admin.register(User)
class UserAdmin(admin.ModelAdmin):
    list_display = ("first_name","username","adress")
    list_display_links = ("first_name","username","adress")
    add_fieldsets = (
        (None, {'fields': ('first_name','username',"adress","img","password")}),
    )
    
    fieldsets = (
        (None, {
            "fields": (
                'first_name','username',"adress","img","password",
            ),
        }),
    )
    
    
@admin.register(Account)
class AccountAdmin(admin.ModelAdmin):
    list_display = ("name","link")
    list_display_links = ("name","link")